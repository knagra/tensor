/* With - denoting an unlit pixel and O denoting a lit white pixel, this script prints:
 * ------------------------O-----------------------
 * -------------------------O----------------------
 * --------------------------O---------------------
 * ---------------------------O--------------------
 * ----------------------------O-------------------
 * -----------------------------O------------------
 * ------------------------------O-----------------
 * -------------------------------O----------------
 * --------------------------------O---------------
 * ---------------------------------O--------------
 * ----------------------------------O-------------
 * -----------------------------------O------------
 * ------------------------------------O-----------
 * -------------------------------------O----------
 * --------------------------------------O---------
 * ---------------------------------------O--------
 * ----------------------------------------O-------
 * -----------------------------------------O------
 * ------------------------------------------O-----
 * -------------------------------------------O----
 * --------------------------------------------O---
 * ---------------------------------------------O--
 * ----------------------------------------------O-
 * -----------------------------------------------O
 */
unsigned char pixel_color(int x, int y, int frame, int color) {
  if (x == y) {
    return 0xF;
  } else {
    return 0x0;
  }
}
